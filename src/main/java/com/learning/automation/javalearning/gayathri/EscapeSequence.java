package com.learning.automation.javalearning.gayathri;

public class EscapeSequence {
	public static void main(String[] args) {
		System.out.println("hello \t welcome to QA Training");
		System.out.println("hello \b welcome to QA Training");
		System.out.println("hello \n welcome to QA Training");
		System.out.println("hello \r welcome to QA Training");
		System.out.println("hello \f welcome to QA Training");
		System.out.println("hello \'welcome to QA Training'");
		System.out.println("hello \"welcome to QA Training\"");
		System.out.println("hello \\ welcome to QA Training");
	}

}
