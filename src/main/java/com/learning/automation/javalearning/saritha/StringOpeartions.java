package com.learning.automation.javalearning.saritha;

public class StringOpeartions {
	public static void main(String[] args) {
		String firstVal = "Saritha";
		String secondVal = "Chappidi";
		//System.out.println(firstVal+secondVal);
		System.out.println(firstVal.concat(" ").concat(secondVal));
		System.out.println(firstVal.concat(" ").concat(secondVal).length());
		System.out.println(firstVal.concat(" ").concat(secondVal).isEmpty());
		System.out.println(firstVal.concat(" ").concat(secondVal).intern());
		System.out.println(firstVal.concat(" ").concat(secondVal).indexOf(secondVal));
		System.out.println(firstVal.concat(" ").concat(secondVal).lastIndexOf(secondVal));



		


		
	}

}
